using UnityEngine;

public class ErrorCardPopUpView : MonoBehaviour
{
	
	public CardPopUpViewModel popUpVM;
	public ErrorCardPopUpViewModel errorPopUpVM;
	
	public ErrorCardPopUpView ()
	{
		this.popUpVM = new CardPopUpViewModel ();
		this.errorPopUpVM = new ErrorCardPopUpViewModel ();
	}
	
	void OnGUI()
	{
		GUI.depth = popUpVM.guiDepth;
		GUILayout.BeginArea(popUpVM.centralWindow);
		{
			GUILayout.BeginVertical(popUpVM.centralWindowStyle);
			{
				GUILayout.FlexibleSpace();
				GUILayout.Label (errorPopUpVM.error,popUpVM.centralWindowTitleStyle);
				GUILayout.FlexibleSpace();
				GUILayout.BeginHorizontal();
				{
					GUILayout.FlexibleSpace();
					if (GUILayout.Button("OK",popUpVM.centralWindowButtonStyle,GUILayout.Width (popUpVM.centralWindow.width*0.3f)))
					{
						gameObject.GetComponent<CardController>().hideErrorCardPopUp();
					}
					GUILayout.FlexibleSpace();
				}
				GUILayout.EndHorizontal();
				GUILayout.FlexibleSpace();
			}
			GUILayout.EndVertical();
		}
		GUILayout.EndArea();
	}
}


